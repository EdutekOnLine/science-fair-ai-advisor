
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { title, description, hypothesis, materials } = await req.json();

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${Deno.env.get('OPENAI_API_KEY')}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: 'You are a scientific advisor helping students improve their science fair projects. Analyze the project and provide constructive feedback, suggestions for improvement, and potential extensions of the experiment. Focus on scientific method, methodology, and educational value.'
          },
          {
            role: 'user',
            content: `Please analyze this science fair project:
              Title: ${title}
              Description: ${description}
              Hypothesis: ${hypothesis || 'Not provided'}
              Materials: ${materials.join(', ')}
              
              Provide feedback and suggestions in the following areas:
              1. Scientific Method
              2. Methodology
              3. Potential Improvements
              4. Related Areas to Explore`
          }
        ],
      }),
    });

    const data = await response.json();
    const analysis = data.choices[0].message.content;

    return new Response(
      JSON.stringify({ analysis }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
    );
  }
});
